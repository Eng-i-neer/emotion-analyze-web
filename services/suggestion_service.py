def get_suggestions_by_emotion(emotion):
    return SUGGESTION_LIBRARY.get(emotion, [])


def get_all_suggestions():
    return SUGGESTION_LIBRARY


def get_personalized_suggestions(recent_emotions):
    """Based on recent mood types, pick relevant suggestions."""
    result = {}
    for emotion in recent_emotions:
        suggestions = SUGGESTION_LIBRARY.get(emotion)
        if suggestions:
            result[emotion] = suggestions
    if not result:
        result['日常'] = [
            '每天花几分钟记录心情，自我观察是情绪管理的第一步',
            '保持规律的作息和适度的运动',
            '和朋友或家人保持联系，社交支持很重要'
        ]
    return result
